// Helper functions
export const getLanguageByName = (name: string): Language | undefined => {
  return SUPPORTED_LANGUAGES.find(lang => lang.name === name);
};

export const getLanguageByCode = (code: string): Language | undefined => {
  return SUPPORTED_LANGUAGES.find(lang => lang.code === code);
};

export const getLanguageDisplayName = (language: Language): string => {
  return `${language.flag} ${language.nativeName}`;
};